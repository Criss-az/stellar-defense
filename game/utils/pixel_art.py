# Dibuja sprites como matrices de píxeles.
# Sin imágenes externas. Todo en código, estilo retro puro.

import pygame

def draw_sprite(surface, matrix, color, accent_color, x, y, scale=3):
    """
    Dibuja un sprite a partir de una matriz 2D.
    0 = vacío | 1 = color principal | 2 = color acento
    scale: tamaño de cada píxel (3 = píxeles grandes y retro)
    """
    for row_i, row in enumerate(matrix):
        for col_i, pixel in enumerate(row):
            if pixel == 0:
                continue
            c = color if pixel == 1 else accent_color
            pygame.draw.rect(
                surface, c,
                (x + col_i * scale,
                 y + row_i * scale,
                 scale, scale)
            )

